package com.entity;

public class Doctor {
    private int id;
    private String fullName;
    private String dob;
    private String qualification;
    private String email;
    private String spec;
    private String mobno;
    private String password;
    private String status;
    private String experience;
    private String consultationFee;
    private String bio;
    private String availability;
    private String rating;

    public Doctor() {}

    public int getId() { return id; }
    public void setId(int id) { this.id = id; }
    public String getFullName() { return fullName; }
    public void setFullName(String fullName) { this.fullName = fullName; }
    public String getDob() { return dob; }
    public void setDob(String dob) { this.dob = dob; }
    public String getQualification() { return qualification; }
    public void setQualification(String qualification) { this.qualification = qualification; }
    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }
    public String getSpec() { return spec; }
    public void setSpec(String spec) { this.spec = spec; }
    public String getMobno() { return mobno; }
    public void setMobno(String mobno) { this.mobno = mobno; }
    public String getPassword() { return password; }
    public void setPassword(String password) { this.password = password; }
    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }
    public String getExperience() { return experience; }
    public void setExperience(String experience) { this.experience = experience; }
    public String getConsultationFee() { return consultationFee; }
    public void setConsultationFee(String consultationFee) { this.consultationFee = consultationFee; }
    public String getBio() { return bio; }
    public void setBio(String bio) { this.bio = bio; }
    public String getAvailability() { return availability; }
    public void setAvailability(String availability) { this.availability = availability; }
    public String getRating() { return rating; }
    public void setRating(String rating) { this.rating = rating; }
}
